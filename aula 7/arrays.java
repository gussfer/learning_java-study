public class arrays {
    public static void main(String[] args) {
        int vet[] = {7, 4, 3, 2}; // cria o vetor e atribui o tamanho
        int cont; // define o contador
        for(cont = 0 ; cont < vet.length ; cont++) // (inicio do contador; maior tamanho do contador; como ele percorre o array)
        {
            System.out.println("Vetor [ " + cont + " ] = " + vet[cont]);
        }
    }
}
